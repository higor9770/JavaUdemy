package entities;

public class Entities2 {

	
	private String nome;
	private double preco;
	
	public Entities2(String nome, double preco) {

		this.nome = nome;
		this.preco = preco;
	}
	
	public String getnome() {
		return nome;
	}
	
	public void setnome(String nome) {
		this.nome = nome;
	}
	
	public double getpreco() {
		return preco;
	}
	
	public void setpreco(double preco) {
		this.preco = preco;
	}
	
	
	
}
